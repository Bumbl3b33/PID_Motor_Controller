/**
  * @brief This function handles TIM7 global interrupt
					 Runs every 50ms
					 Runs instance of model with inputs and outputs
  */
void TIM7_IRQHandler(void) 
{
  HAL_TIM_IRQHandler(&htim7); 
  
	//Model Inputs
	rtU.speedInput=ADC_value;  		//user set speed
	rtU.shaftPos_count=TIM2->CNT;	//current motor position
		
	rt_OneStep();	//Running Instance of Model

	//OUPUT - Direction of Motor
	if (rtY.direction == 1){//forward
		HAL_GPIO_WritePin(GPIOD,GPIO_PIN_12,(GPIO_PinState)1);
		HAL_GPIO_WritePin(GPIOD,GPIO_PIN_13,(GPIO_PinState)0);
	}
	else{//reverse
		HAL_GPIO_WritePin(GPIOD,GPIO_PIN_12,(GPIO_PinState)0);
		HAL_GPIO_WritePin(GPIOD,GPIO_PIN_13,(GPIO_PinState)1);
	}
	
	//OUTPUT - PWM
	__HAL_TIM_SetCompare(&htim3,TIM_CHANNEL_4,rtY.PWM_dutyCommand);
	
	nT ++; //IRQ count
}
	
